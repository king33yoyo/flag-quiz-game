// Helper functions
export function getRandomCountries(count: number, difficulty?: Country['difficulty']): Country[] {
  let filteredCountries = countries;
  
  if (difficulty) {
    filteredCountries = countries.filter(c => c.difficulty === difficulty);
  }
  
  // Shuffle and return requested count
  return filteredCountries
    .sort(() => 0.5 - Math.random())
    .slice(0, count);
}

export function getCountryById(id: string): Country | undefined {
  return countries.find(c => c.id === id);
}

export function getCountriesByDifficulty(difficulty: Country['difficulty']): Country[] {
  return countries.filter(c => c.difficulty === difficulty);
}

export function getCountriesByContinent(continent: string): Country[] {
  return countries.filter(c => c.continent === continent);
}

export function getContinents(): string[] {
  const continents = [...new Set(countries.map(c => c.continent))];
  return continents.sort();
}
